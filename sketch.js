function setup() {
  createCanvas(1280, 640);

  resetTiles();
  resetInventorySelected();
}

function draw() {
  x = mouseX / 32;
  y = mouseY / 32;

  if (isTheGameOn) {
    gameScreen();
  }

  else introScreen();
}

function mousePressed() {
  if (isTheGameOn) {
    if (mouseX > 1225.6 && mouseX < 1264 && mouseY > 16 && mouseY < 48) {
      isTakingAPicture = true;
      saveFrames('my-map', 'png', 1, 1);
    }

    if (mouseX > 16 && mouseX < 85.93 && mouseY > 16 && mouseY < 48) {
      isTheGameOn = false;
    }

    if (mouseX > 554.67 && mouseX < 597.33 && mouseY > 586.67 && mouseY < 629.34) {
      isGrassSelected = true;
      isAxSelected = false;
      isHoeSelected = false;
      isSeedSelected = false;
    }

    if (mouseX > 597.33 && mouseX < 640 && mouseY > 586.67 && mouseY < 629.34) {
      isGrassSelected = false;
      isAxSelected = true;
      isHoeSelected = false;
      isSeedSelected = false;
    }

    if (mouseX > 640 && mouseX < 682.67 && mouseY > 586.67 && mouseY < 629.34) {
      isGrassSelected = false;
      isAxSelected = false;
      isHoeSelected = true;
      isSeedSelected = false;
    }

    if (mouseX > 682.67 && mouseX < 725.34 && mouseY > 586.67 && mouseY < 629.34) {
      isGrassSelected = false;
      isAxSelected = false;
      isHoeSelected = false;
      isSeedSelected = true;
    }

    for (let i = 0; i < 40; i++)
      for (let j = 0; j < 20; j++) {
        if (floor(x) == i && floor(y) == j && tilesMap[j][i] == 0 && isHoeSelected && treesMap[j][i] == 0)
          tilesMap[j][i] = 1;

        if (floor(x) == i && floor(y) == j && tilesMap[j][i] == 1 && isGrassSelected && treesMap[j][i] == 0)
          tilesMap[j][i] = 0;

        if (floor(x) == i && floor(y) == j && tilesMap[j][i] == 1 && isSeedSelected && treesMap[j][i] == 0)
          treesMap[j][i] = 1;

        if (floor(x) == i && floor(y) == j && isAxSelected) {
          if (treesMap[j][i] <= 4 || treesMap[j][i] == 6) {
            treesMap[j][i] = 0;
            treesTime[j][i] = 0;
          }
          if (treesMap[j][i] == 5) treesMap[j][i] = 6;
        }
      }
  }

  else {
    if (mouseX > 489 && mouseX < 600 && mouseY > 411 && mouseY < 498) {
      resetInventorySelected();
      resetTiles();
      isTheGameOn = true;
    }

    if (mouseX > 680 && mouseX < 791 && mouseY > 411 && mouseY < 498) {
      isTheGameOn = true;
    }
  }
}

